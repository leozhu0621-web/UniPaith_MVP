def _apply_catalogue_descriptions() -> None:
    """Replace embedded descriptions with verified catalogue prose."""
    for prog in PROGRAMS:
        slug = prog.get("slug")
        if slug and slug in CATALOGUE_DESCRIPTIONS:
            prog["description"] = CATALOGUE_DESCRIPTIONS[slug]


_apply_catalogue_descriptions()


def _assert_catalog_descriptions_clean() -> None:
    from unipaith.profile_standard.anti_stub import analyze, machine_artifacts

    rows = [
        {"program_name": p["program_name"], "description": p.get("description") or ""}
        for p in PROGRAMS
    ]
    report = analyze(rows)
    arts = machine_artifacts(rows)
    if not report.is_clean or arts:
        raise ValueError(
            f"Stanford catalog description gate failed: {report.summary()} artifacts={len(arts)}"
        )


_assert_catalog_descriptions_clean()

